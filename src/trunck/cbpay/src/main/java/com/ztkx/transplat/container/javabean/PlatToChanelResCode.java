package com.ztkx.transplat.container.javabean;

public class PlatToChanelResCode {

	private String platcode;
	private String tranForm;
	private String chanelcode;
	private String chaneldes;
	public String getPlatcode() {
		return platcode;
	}
	public void setPlatcode(String platcode) {
		this.platcode = platcode;
	}
	
	public String getTranForm() {
		return tranForm;
	}
	public void setTranForm(String tranForm) {
		this.tranForm = tranForm;
	}
	public String getChanelcode() {
		return chanelcode;
	}
	public void setChanelcode(String chanelcode) {
		this.chanelcode = chanelcode;
	}
	public String getChaneldes() {
		return chaneldes;
	}
	public void setChaneldes(String chaneldes) {
		this.chaneldes = chaneldes;
	}
	
}
