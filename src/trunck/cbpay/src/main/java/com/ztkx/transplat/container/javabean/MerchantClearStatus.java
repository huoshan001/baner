package com.ztkx.transplat.container.javabean;

public class MerchantClearStatus {
	
	private String merchantid;
	private String clear_date;
	private String clear_time;
	private String bus_date;
	private String status;
	
	public String getMerchantid() {
		return merchantid;
	}
	public void setMerchantid(String merchantid) {
		this.merchantid = merchantid;
	}
	public String getClear_date() {
		return clear_date;
	}
	public void setClear_date(String clear_date) {
		this.clear_date = clear_date;
	}
	public String getClear_time() {
		return clear_time;
	}
	public void setClear_time(String clear_time) {
		this.clear_time = clear_time;
	}
	public String getBus_date() {
		return bus_date;
	}
	public void setBus_date(String bus_date) {
		this.bus_date = bus_date;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}

}
