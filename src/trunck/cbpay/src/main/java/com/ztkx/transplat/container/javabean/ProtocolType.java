package com.ztkx.transplat.container.javabean;

public class ProtocolType {
	
	private String protocoltype;
	private String protocolconfig;
	private String protocolparse;
	private String protocolprocess;
	public String getProtocoltype() {
		return protocoltype;
	}
	public void setProtocoltype(String protocoltype) {
		this.protocoltype = protocoltype;
	}
	public String getProtocolconfig() {
		return protocolconfig;
	}
	public void setProtocolconfig(String protocolconfig) {
		this.protocolconfig = protocolconfig;
	}
	public String getProtocolparse() {
		return protocolparse;
	}
	public void setProtocolparse(String protocolparse) {
		this.protocolparse = protocolparse;
	}
	public String getProtocolprocess() {
		return protocolprocess;
	}
	public void setProtocolprocess(String protocolprocess) {
		this.protocolprocess = protocolprocess;
	}
	
	

}
