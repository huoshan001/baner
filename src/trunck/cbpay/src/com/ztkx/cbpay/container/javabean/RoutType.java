package com.ztkx.cbpay.container.javabean;

public class RoutType {
	private String routeres;
	private String routeField;
	private String routetype;
	private String tran_opr;
	
	public String getRouteres() {
		return routeres;
	}
	public void setRouteres(String routeres) {
		this.routeres = routeres;
	}
	public String getRouteField() {
		return routeField;
	}
	public void setRouteField(String routeField) {
		this.routeField = routeField;
	}
	public String getRoutetype() {
		return routetype;
	}
	public void setRoutetype(String routetype) {
		this.routetype = routetype;
	}
	public String getTran_opr() {
		return tran_opr;
	}
	public void setTran_opr(String tran_opr) {
		this.tran_opr = tran_opr;
	}
}
